export function updateLanguage(lang) {
  const t = translations[lang]
  if (!t) return
  for (const key in t) {
    const element = document.getElementById(key)
    if (element) {
      if (key === "targetAUC24") {
        const span = element.querySelector("span")
        if (span) span.textContent = t[key]
      } else {
        element.textContent = t[key]
      }
    }
  }
}
